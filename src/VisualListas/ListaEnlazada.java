   private void obtenerListaEnlazada() {
    // Actualizar el contenido de la lista visualmente en un JOptionPane
    StringBuilder contenidoLista = new StringBuilder();
    Nodo actual = lista.getCabeza();
    while (actual != null) {
        contenidoLista.append(actual.getValor()).append(" -> ");
        actual = actual.getSiguiente();
    }

    // Mostrar la lista en un JOptionPane
    JOptionPane.showMessageDialog(this, "Lista actual:\n" + contenidoLista.toString(), "Lista Enlazada", JOptionPane.PLAIN_MESSAGE);
}
    
    
  

