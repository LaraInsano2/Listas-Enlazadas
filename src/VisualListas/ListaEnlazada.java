    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
     String textoBuscar = txtBuscar.getText();
    if (!textoBuscar.isEmpty()) {
        try {
            int numeroBuscar = Integer.parseInt(textoBuscar);
            boolean encontrado = lista.busqueda(numeroBuscar);
            if (encontrado) {
                JOptionPane.showMessageDialog(null, "El número " + numeroBuscar + " está en la lista.");
            } else {
                JOptionPane.showMessageDialog(null, "El número " + numeroBuscar + " no está en la lista.");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Ingrese un número entero válido.");
            // Manejo de error si la entrada no es un número entero
        }
    } else {
        JOptionPane.showMessageDialog(null, "Ingrese un número para buscar en la lista.");
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
    String textoEliminar = txtEliminar.getText();
    if (!textoEliminar.isEmpty()) {
        try {
            int numeroEliminar = Integer.parseInt(textoEliminar);
            boolean encontrado = false;
            Nodo actual = lista.cabeza;
            int posicion = 0;

            while (actual != null) {
                if (actual.valor == numeroEliminar) {
                    lista.eliminacionEnPosicion(posicion);
                    encontrado = true;
                    JOptionPane.showMessageDialog(null, "Número eliminado de la lista.");
                    obtenerListaEnlazada();
                    break;
                }
                actual = actual.siguiente;
                posicion++;
            }

            if (!encontrado) {
                JOptionPane.showMessageDialog(null, "No se ha encontrado el número en la lista para eliminar.");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Ingrese un número entero válido.");
            // Manejo de error si la entrada no es un número entero
        }
    } else {
        JOptionPane.showMessageDialog(null, "Ingrese un número para eliminar de la lista.");
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

